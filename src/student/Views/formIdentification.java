    public void start() {
        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (time > 0) {
                    currentTime.setText(String.valueOf(time--));
                } else {

                    dispose();
                    ((Timer) e.getSource()).stop();
                }
            }
        });
        timer.start();
    }

